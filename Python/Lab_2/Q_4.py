# 4. Compute 
import math

# a) 5 to the power of 8
result_a = 5**8
print("5 to the power of 8:", result_a)

# b) square root of 400
result_b = math.sqrt(400)
print("Square root of 400:", result_b)

# c) exponent of 5 (assuming natural logarithm)
result_c = math.log(5)
print("Exponent of 5 (natural logarithm):", result_c)

# d) Logarithm of 625 base 5
result_d = math.log(625, 5)
print("Logarithm of 625 base 5:", result_d)
